function [y, i, resvec] = relax(A, b, w, tol, maxit) try
chol(A); catch exeption
throw(MException(exeption.identifier, '??????? ?? ???????? ???????????? ?/??? ???????????? ????????????'))
end
if nargin < 5, maxit = 1e5; end 
if nargin < 4, tol = 1e-10; end 
if nargin < 3, w = 1; end
if nargout > 2, resvec = zeros(maxit, 1); end

resvec = zeros(maxit, 1); % ?????? ???? ???????
y = b./diag(A); 
for i = 1:maxit
    for j = 1:numel(b)
        y(j) = (1-w)*y(j) + w*(b(j) - A(j,1:j-1)*y(1:j-1) - A(j,j+1:end)*y(j+1:end))/A(j,j);
    end

    if nargout > 2, resvec(i) = norm(A*y - b, Inf); end 
    if norm(A*y - b, Inf) < tol, break; 
end
 
end
if nargout > 2, resvec = resvec(1:i); end 
end
